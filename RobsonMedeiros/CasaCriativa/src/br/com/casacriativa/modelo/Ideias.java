package br.com.casacriativa.modelo;

public class Ideias {
	private int idIdeia;
	private String nomeIdeia;
	private String categoriaIdeia;
	private String descricaoIdeia;
	

	
	public String getNomeIdeia() {
		return nomeIdeia;
	}
	public void setNomeIdeia(String nomeIdeia) {
		this.nomeIdeia = nomeIdeia;
	}
	public String getCategoriaIdeia() {
		return categoriaIdeia;
	}
	public void setCategoriaIdeia(String categoriaIdeia) {
		this.categoriaIdeia = categoriaIdeia;
	}
	public String getDescricaoIdeia() {
		return descricaoIdeia;
	}
	public void setDescricaoIdeia(String descricaoIdeia) {
		this.descricaoIdeia = descricaoIdeia;
	}
	public int getIdIdeia() {
		return idIdeia;
	}
	public void setIdIdeia(int idIdeia) {
		this.idIdeia = idIdeia;
	}

}
